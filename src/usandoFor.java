public class usandoFor {
    public static void main(String[] args) {
        //Imprimindo todos os numeros pares de 0 até 16
        // for = "repita isso x vezes.

        for(int i = 0; i <= 16; i++){ // Repita para os numeros de 0 a 16
            if (i % 2 == 0){ // Se o resto da divisão de i = 0 , ele é par!
                System.out.println(i); // Imprima I, que serão os numeros pares.
            }
        }
    }
}
